                'estado' => 0,
                'id_alumno' => $request->nro_documento,
                'fecha_limite' => $request->fecha_limite,
                ]);
        Session::flash('message', 'Autorizacion guardada.');
        return Redirect::to('/admin/autorizacion');
    }

